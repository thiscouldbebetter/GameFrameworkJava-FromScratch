
package GameFramework.Utility;

public class Quadruple<T,U,V,W,X>
{
	public T first;
	public U second;
	public V third;
	public W fourth;
	public X fifth;

	public Triple(T first, U second, V third, W fourth, X fifth)
	{
		this.first = first;
		this.second = second;
		this.third = third;
		this.fourth = fourth;
		this.fifth = fifth;
	}
}
